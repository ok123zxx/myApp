package com.springboot.myapp.entity;

public class Store {

	private String id;
	
	private String keyT;
	
	private String val;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getKeyT() {
		return keyT;
	}

	public void setKeyT(String keyT) {
		this.keyT = keyT;
	}

	public String getVal() {
		return val;
	}

	public void setVal(String val) {
		this.val = val;
	}
}
